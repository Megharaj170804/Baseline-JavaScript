// JavaScript Null and Undefined Values Example

let valueNull = null;
let valueUndefined;

function showNullUndefinedExamples() {
    document.getElementById("nullEx").textContent = 'let valueNull = null; // valueNull is ' + valueNull;
    document.getElementById("undefinedEx").textContent = 'let valueUndefined; // valueUndefined is ' + valueUndefined;
    document.getElementById("typeofNull").textContent = 'typeof valueNull = ' + typeof valueNull;
    document.getElementById("typeofUndefined").textContent = 'typeof valueUndefined = ' + typeof valueUndefined;
}
